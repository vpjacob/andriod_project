package com.doormaster.topkeeper.bean;

/**
 * 类名称：DoorKeyBean.java <br>
 * 内容摘要： 开门密码实体类<br>
 */
public class DevKeyBean {
    /**
     * 设备所属小区编号，唯一键，用于区分小区
     */
    private String community_code;
    /**
     * 设备名称
     */
    private String dev_name;
    /**
     * 设备序列号，远程开门使用
     */
    private String dev_sn;
    /**
     * 可视对讲设备音视频账号
     */
    private String dev_voip_account;
    /**
     * type
     */
    private int dev_type = 2;


    //顺序号
    private int orderNum = 1100;

    private String username;

    public int getOrderNum() {
        return orderNum;
    }

    public void setOrderNum(int orderNum) {
        this.orderNum = orderNum;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getCommunity_code() {
        return community_code;
    }

    public void setCommunity_code(String community_code) {
        this.community_code = community_code;
    }

    public String getDev_name() {
        return dev_name;
    }

    public void setDev_name(String dev_name) {
        this.dev_name = dev_name;
    }

    public String getDev_sn() {
        return dev_sn;
    }

    public void setDev_sn(String dev_sn) {
        this.dev_sn = dev_sn;
    }

    public String getDev_voip_account() {
        return dev_voip_account;
    }

    public void setDev_voip_account(String dev_voip_account) {
        this.dev_voip_account = dev_voip_account;
    }

    public int getDev_type() {
        return dev_type;
    }

    public void setDev_type(int dev_type) {
        this.dev_type = dev_type;
    }

    @Override
    public String toString() {
        return "DevKeyBean{" +
                "community_code='" + community_code + '\'' +
                ", dev_name='" + dev_name + '\'' +
                ", dev_sn='" + dev_sn + '\'' +
                ", dev_voip_account='" + dev_voip_account + '\'' +
                ", orderNum=" + orderNum +
                ", USERNAME='" + username + '\'' +
                '}';
    }
  
}
